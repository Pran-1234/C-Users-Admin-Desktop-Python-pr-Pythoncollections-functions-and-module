# Importing the math module
import math

# Using sqrt() to find the square root of a number
number = 16
sqrt_result = math.sqrt(number)
print(f"The square root of {number} is: {sqrt_result}")

# Using ceil() to round up to the nearest integer
ceil_result = math.ceil(4.3)
print(f"The ceiling of 4.3 is: {ceil_result}")

# Using floor() to round down to the nearest integer
floor_result = math.floor(4.7)
print(f"The floor of 4.7 is: {floor_result}")
